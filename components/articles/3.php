   <h3>EMS系统介绍</h3>

   <p>企业能源管理系统采用分布式计算机信息系统，由一级或多级监控中心组成。系统主要由三部分组成：能源管控中心、能源信息网络和现场监控单元。 企业能源管理系统主要的管控对象为企业生产经营活动所涉及到的水、电、气、风、油等各种能源介质。能源管理系统对企业的电力系统、动力系统（燃气、热力、氧氮氩等）、水道系统和部分环保数据实行集中监控和管理，从而实现能源系统的统一集中调度控制和经济结算。通过对能源系统实行集中监测和控制，一方面，实现从能源数据采集——过程控制——能源介质消耗分析——能源管理等全过程自动化、高效化、科学化管理、使能源管理与能源生产、使用的全过程有机结合起来，提升能源管理的整体水平。 另一方面，系统对生产过程中所发生的能源信息进行准确汇总，同时对能源采集设备的运行转况进行实时监控。基于系统强大的能源生产信息数据、制造执行系统的综合生产信息及能源信息、ERP销售成本和能源业务日常管理等信息数据，运用先进的数据处理与分析技术，实现能源系统的离线生产分析和管理功能，包括能源生产管理统计报表、平衡分析、质量管理、实绩管理、运行支持管理、预测分析等功能。</p>

 
 
 
 

