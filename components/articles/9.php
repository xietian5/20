   <h3>系统概述</h3>

   <p>该系统基于GE智能平台先进、成熟的SCADA软件平台和公司行业
SCADA经验，通过Modbus以太网将现场计量设备采集监控。本系
统负责采集、显示各能源设备的监控数据，同时将重要数据进行存
储，系统可查看重要能源数据的实时趋势、历史趋势，并按需生成
各种曲线、报表（日报、月报、年报等）。
