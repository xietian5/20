   <h3>DMS系统介绍</h3>

   <p>建设生产调度管理系统（DMS）的总体目标是为市政公司提供一套具备一定智能调度能力并在行业处于领先水平的综合调度管理平台，通过将生产调度所需的各个子系统通过一个整合的综合性应用平台进行数据调度和集中展现，从而进一步提升生产调度中心的运营管理水平，提高工作效率和调度能力，保证管网和站点的安全、稳定、连续运行。</p>
   <p>

DIMASIS为市政公司各个岗位的用户在Web浏览器、桌面和移动终端上进行生产调度管理提供了一个完整、智能、可伸缩的框架。DIMASIS是一整套生产调度所需要的子系统的集合，并通过对它们的整合，构成了一个智能的管理平台。 </p>

  <p>
    通过生产调度管理系统的实施，能满足和实现下列功能，并达到行业领先水平：
  </p>

  <ul>
    <li style="list-style-type:square">使调度人员利用一个系统平台掌控整个燃气或水务系统的运行情况，并通过该系统实现对管网和各站点的生产调度工作的在线处理；</li>
    <li style="list-style-type:square">通过集成的SCADA、CIS/TCMS、GIS、GPS/AVL以及MMS等功能，使得调度及维护人员可以清晰、明确、快速的应对突发事件以及日常维护需求；</li>
    <li style="list-style-type:square">实现公司日常生产管理的信息化，包括调度台帐、远程查询、各类报表等；</li>
    <li style="list-style-type:square">对生产数据进行深入的收集、统计、汇总和分析，并构建数据分析模型挖掘有效数据，为用户决策提供丰富、可靠、直观的依据。</li>
  </ul>

 
 
 
 

