   <h3>广播系统</h3>

   <p>广播系统包括车间广播系统和区域呼叫系统。车间广播系统根据配置定时在WE2车间播放设定音乐，需要时可以通过车间级
麦克风进行全车间呼叫。区域广播系统为各自动线或区域内呼叫系统，由于车间生产时噪音较大，此系统方便区域内不同工
位间进行沟通。</p>

 
 
 
 

