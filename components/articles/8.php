   <h3>PMC系统</h3>

   <p>PMC系统即生产过程监控系统，是和WE2科车间现场18套设备PLC组成以太网局域网，以便实时监控设备状态，在办公室
就可以随时了解现场设备状况，在发生异常时可以做出及时响应；同时还可以实时采集和记录各套设备的生产数据，如报警
信息、运行状态、每台车的生产信息（生产时间、车型、节拍等），通过这些数据可以制作出各种报表，分析出设备的运行
能力、产品的生产时间等，对设备的改善和生产决策提供了大量的数据依据。
</p>

