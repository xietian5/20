   <h3>MES系统介绍</h3>
   <p><span class="b">MES制造执行系统</span>是一套面向制造企业车间执行层的生产信息化管理系统。MES可以为企业提供包括工艺工序管理、资源物料管理、生产单元分配、作业人员管理、产品跟踪管理、文档管理、设备管理、计划排程管理、质量管理、生产单元分配、产品跟踪管理、项目看板管理、数据采集、性能分析、报警等管理模块，为企业打造一个扎实、可靠、全面、可行的制造协同管理平台。对生产过程的管理和优化，从而全面监控生产过程，生产数据，包括工序、作业、设备、物流、订单等等的全面监控，从而提高生产效率和良率。</p>

   <p>
     MES能通过信息的传递，对从订单下达开始到产品完成的整个产品生产过程监控并进行优化的管理，对工厂发生的实时事件，及时作出相应的反应和报告，并用当前准确的数据分析进行相应的指导和处理。
   </p>


