     海上丝路遍机遇，联盟春风入羊城
    </div> 
    <div class="newstitle2"> 
     <span>发表于：2017-05-08 13:55</span> 
    </div> 
   </div> 
  </div> 
  <div class="about"> 
   <p> 2015年4月24日，“路面机械专业品牌联盟华南三省（广州）发布会”在广州市嘉逸皇冠酒店隆重召开。路机联盟首任轮值主席、浙江美通筑路机械股份有限公司董事长仇德胜、路机联盟理事、青岛科泰重工机械有限公司总经理姚录廷、江阴市鑫海公路机械材料有限公司董事长周永红、陕西建设机械股份有限公司常务副总经理贺卫东、广州誉畅华机械设备有限公司董事长刘世杰以及华南三省的公路管理部门、道路建设养护工程施工单位、周边道路工程施工单位的代表逾200人出席并见证了中国&middot;路面机械专业品牌联盟与广州誉畅华机械设备有限公司合作的开端。</p> 
   <p> <br /> 　　仇德胜主席在致辞中表示，“一带一路”正在由战略构想步入全面实施阶段，对装备制造业来说是大机遇、大挑战。路机联盟自成立后，受到了行业内的极大关注，得到了社会各界的关怀和肯定。联盟的四家企业在合作中相互学习、共同进步，在以为用户提供优质、可靠的产品和周到、细致的服务为前提下，各自纵向深入发展自己的产品，做精做专。提供成套化设备以期更好地服务用户，打造国内一流、国际知名品牌。</p> 
   <p> <br /> 　　会上，路面机械专业品牌联盟理事、青岛科泰重工机械有限公司总经理姚录廷就路机联盟的基本情况和当前的工作进展作了主题发言。浙江美通筑路机械股份有限公司市场部部长刘伟、青岛科泰重工机械有限公司副总经理李顺舟、江阴市鑫海公路机械材料有限公司总经理周永君、陕西建设机械股份有限公司常务副总经理贺卫东等分别就各自公司和产品的优势作了报告。</p> 
   <p> <br /> 　　发布会上还举行了路机联盟成套设备区域代理商授牌仪式。广州誉畅华机械设备有限公司正式成为路机联盟成套设备的区域代理。路面机械专业品牌联盟理事、江阴市鑫海公路机械材料有限公司董事长周永红为广州誉畅华机械设备有限公司总经理刘伟授牌。</p> 
   <p> <br /> 　　在路机联盟路面成套设备静态展示和广州誉畅华公司参观时，各位与会代表对路机联盟产品及代理商的专业程度给予了高度评价。在过硬的产品和强有力的销售政策带动下，现场一度发生火爆的“抢购”现象，达成意向订单近千万元。</p> 
   <p> <br /> 　　习近平总书记提出的“一带一路”的构想，就是要弘扬丝路精神，树立开放发展、合作发展、共赢发展理念，向前展望、凝心聚力，构筑沿线各国经贸和文化交流大通道。这与路机联盟“精诚合作、开拓创新；平等互利，优势互补；品质第一，共同发展”的宗旨高度契合。通过本次路机联盟成套产品在广州的代理商落地，使得路机联盟在积极主动融入国家“一带一路”的战略进程中迈出了重要的一步。（本文来自科泰重工）</p> 
